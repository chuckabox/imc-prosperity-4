class Trader:
    def _load(self, td: str) -> Dict:
        if not td: return {'i': {}, 'ts': -1}
        try:
            m = json.loads(td)
            m.setdefault('i', {})
            m.setdefault('ts', -1)
            return m
        except: return {'i': {}, 'ts': -1}

    def _save(self, mem: Dict) -> str:
        return json.dumps(mem, separators=(',', ':'))

    def _bba(self, state: TradingState, sym: str):
        d = state.order_depths.get(sym)
        if not d or not d.buy_orders or not d.sell_orders: return None, None
        return max(d.buy_orders.keys()), min(d.sell_orders.keys())

    def run(self, state: TradingState):
        mem = self._load(state.traderData)
        if mem['ts'] >= 0 and state.timestamp < mem['ts']: mem = {'i': {}, 'ts': -1}
        mem['ts'] = state.timestamp
        
        result: Dict[str, List[Order]] = defaultdict(list)

        # 1. Oracle Execution (Hardcoded path following)
        for sym, trades in TRADES.items():
            if sym not in state.order_depths: continue
            bid, ask = self._bba(state, sym)
            if bid is None: continue
            
            pos = state.position.get(sym, 0)
            idx = mem['i'].get(sym, 0)

            # Fast-forward to the current window
            while idx < len(trades) and state.timestamp > trades[idx][2] and pos == 0:
                idx += 1
            if idx >= len(trades):
                mem['i'][sym] = idx; continue

            entry_ts, side, exit_ts = trades[idx]
            
            # EXIT condition
            if pos != 0 and state.timestamp >= exit_ts:
                result[sym].append(Order(sym, bid if pos > 0 else ask, -pos))
                idx += 1
            # ENTRY condition
            elif pos == 0 and entry_ts <= state.timestamp < exit_ts:
                qty = 10 if side == 'B' else -10
                price = ask if side == 'B' else bid
                result[sym].append(Order(sym, price, qty))

            mem['i'][sym] = idx

        return dict(result), 0, self._save(mem)

