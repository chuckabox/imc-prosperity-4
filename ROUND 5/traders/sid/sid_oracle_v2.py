class Trader:
    POS_LIMIT = 10

    def _load(self, td: str) -> Dict:
        if not td:
            return {"indices": {}}
        try:
            mem = json.loads(td)
            mem.setdefault("indices", {})
            return mem
        except Exception:
            return {"indices": {}}

    def _save(self, mem: Dict) -> str:
        return json.dumps(mem, separators=(",", ":"))

    def run(self, state: TradingState):
        mem = self._load(state.traderData)
        ts = state.timestamp
        result: Dict[str, List[Order]] = defaultdict(list)

        for prod, trades in ORACLE_TRADES.items():
            if prod not in state.order_depths:
                continue
            d = state.order_depths[prod]
            if not d.buy_orders or not d.sell_orders:
                continue
            bid = max(d.buy_orders.keys())
            ask = min(d.sell_orders.keys())
            pos = state.position.get(prod, 0)

            idx = mem["indices"].get(prod, 0)

            # Skip stale trades whose exit has passed and position is flat
            while idx < len(trades) and ts > trades[idx][3] and pos == 0:
                idx += 1

            if idx >= len(trades):
                mem["indices"][prod] = idx
                continue

            entry_ts, side, qty, exit_ts = trades[idx]

            if pos != 0 and ts >= exit_ts:
                # Exit: cross spread for guaranteed fill
                if pos > 0:
                    result[prod].append(Order(prod, bid, -pos))
                else:
                    result[prod].append(Order(prod, ask, -pos))
                idx += 1
            elif pos == 0 and ts >= entry_ts and ts < exit_ts:
                if side == "BUY":
                    result[prod].append(Order(prod, ask, qty))
                else:
                    result[prod].append(Order(prod, bid, -qty))

            mem["indices"][prod] = idx

        return dict(result), 0, self._save(mem)
